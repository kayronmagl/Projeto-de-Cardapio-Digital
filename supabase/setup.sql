-- Schema para o Cardápio Digital (Supabase)

create table if not exists public.categorias (
  slug text primary key,
  nome text not null,
  ordem integer not null default 0,
  ativa boolean not null default true,
  is_combo boolean not null default false,
  permite_adicionais boolean not null default false
);

create table if not exists public.adicionais (
  id text primary key,
  nome text not null,
  preco numeric(10, 2) not null default 0,
  ativo boolean not null default true,
  ordem integer not null default 0
);

create table if not exists public.produtos (
  id integer generated by default as identity primary key,
  nome text not null,
  descricao text not null default '',
  preco numeric(10, 2) not null default 0,
  categoria_slug text not null references public.categorias(slug),
  disponivel boolean not null default true,
  ordem integer not null default 0
);

create table if not exists public.produto_adicionais (
  produto_id integer not null references public.produtos(id) on delete cascade,
  adicional_id text not null references public.adicionais(id) on delete cascade,
  primary key (produto_id, adicional_id)
);

create table if not exists public.combos_config (
  id smallint primary key default 1,
  ativo boolean not null default true,
  desconto numeric(10, 2) not null default 0,
  categorias_base text[] not null default '{}',
  categoria_bebida_slug text not null references public.categorias(slug)
);

create table if not exists public.loja_config (
  id smallint primary key default 1,
  numero_whatsapp text not null,
  chave_pix text not null,
  pix_titular text not null,
  pix_banco text not null,
  taxa_entrega numeric(10, 2) not null default 0,
  endereco_retirada text not null,
  bairros_com_entrega text[] not null default '{}',
  bairros_sem_entrega text[] not null default '{}',
  ids_indisponiveis integer[] not null default '{}'
);

create table if not exists public.horario_funcionamento (
  dia_semana smallint primary key,
  abre time not null,
  fecha time not null,
  ativo boolean not null default true
);

